    <h1>His Holiness Hazrat Mirza Masroor Ahmad</h1>
    <p>The Worldwide Head of the Ahmadiyya Muslim Community</p>
    <div class="content-wrapper">
        <div class="details-wrapper">
            <p>His Holiness Hazrat Mirza Masroor Ahmad is the worldwide leader of the Ahmadiyya Muslim Community. Elected to office in April 2003, His Holiness is the fifth Khalifa (Caliph) of the Ahmadiyya Community following the demise of the founder in 1908 .He leads the global community that is now established in over 213 countries with tens of millions of members worldwide.</p>
            <p>His Holiness Hazrat Mirza Masroor Ahmad is internationally respected and has repeatedly spoken on the need for peace. His latest parliamentary address was delivered in Canada, on 17th October 2016 where His Holiness emphasised the need for justice as a means to peace. he stated:</p>
            <p>“Irrespective of differences of background, I believe that on the basis of humanity, we are all joined together, and should therefore be united. All people and all organisations, must collectively endeavour to uphold human values, and strive to make the world in which we live, a better and far more harmonious place.”</p>
            <p>His Holiness has travelled globally to promote dialogue and understanding. He regularly meets presidents, prime ministers, other heads of state, parliamentarians and ambassadors of state.</p>
