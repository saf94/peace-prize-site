<div class="dropdown-wrapper process-dropdown-wrapper">
    <h2>Process</h2>
    <div class="dropdown-content process-dropdown-content">
        <ol>
            <li>
                The nominee (ie person being nominated) must be alive at time of nomination.
            </li>
            <li>
                All nominations must be submitted in English.
            </li>
            <li>
                All nominations must be made on the attached form.
            </li>
            <li>
                Nominations can be made by anyone (Ahmadi or non-Ahmadi individuals) and must be submitted through the National Amir of the country in which the person making the nomination resides.

            </li>
            <li>
                The National Amir may provide comments on any nomination (if he wishes to do so) and must forward all nominations to the central committee.
            </li>
            <li>
                All deliberations of the central committee will remain confidential.
            </li>
        </ol>
    </div>
</div>