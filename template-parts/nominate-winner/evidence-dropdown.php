    <h2>Evidence</h2>
    <div class="dropdown-content evidence-dropdown-content">
        <p>
            The evidence provided to support any nomination must be:
        </p>
        <ol>
            <li>
                Objective; and
            </li>
            <li>
                In the public domain.
            </li>
        </ol>
        <p>
            Types of evidence could include:
        </p>
        <ul>
            <li>
                Quotes from a reputable organisation’s website.
            </li>
            <li>
                Copies or referenced quotes from local, regional and national press.
            </li>
            <li>
                Details of any other awards given, if so cite them as well
            </li>
            <li>
                Other reputable web source or other sources eg NGO, Governmental etc (Web sources should exclude wikipedia, blogs and unreferenced data if possible). When sourcing web pages, you must state the date that you last accessed the page.
            </li>
        </ul>
        <p>
            Hard copies of the evidence (including print outs of relevant web pages) must be provided with the nomination form.
        </p>
        <p>
            A specimen form can be found here (link to ANNEX 2) that shows a completed form and the type of evidence that could be presented. The specimen form is not exhaustive and is only a sample provided as a guide to help the person making the nomination complete the form.
        </p>
        <p>
            The Ahmadiyya Muslim Peace Prize Committee (the ‘central committee’) that will assess the nominations reserves the right to revoke a nomination and review its decision at any stage if new evidence comes to light (based on said criteria) that would have affected its assessment of the nomination.
        </p>

    </div>
</div>