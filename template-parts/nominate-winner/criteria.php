            <h2>Criteria</h2>
            <p class="description">
                All nominations will be appraised in light of the Ahmadiyya Muslim Jama’at’s (Islam’s) ideological framework and will also consider:
            </p>
            <ol>
                <li>How has the individual’s/organisation’s work had a direct impact or led to direct change?</li>
                <li>How has the impact/change advanced the cause of peace?
                    Peace, or types of ‘peace’ applicable would be economic, social, religious, national or international. This could be achieved in a number of ways, including: </li>
                </li>
            </ol>
            <ul>
                <li>Negotiation/mediation to bring peace to an area;</li>
                <li>Implementation of any major charitable project for the benefit of humanity;</li>
                <li>Development of ideas (eg economic, scientific, political or social) that result in or play a key role in the promotion of peace;</li>
                <li>Playing a leading role in human rights or other causes to highlight particular issues and enable action to promote peace;</li>
            </ul>
            <p>Nominees therefore may be from a wide range of backgrounds depending upon the work they have carried out so could include (but are not limited to) journalists, authors, scientists, academics, charity workers, religious leaders, lobbyists, businesspersons, parliamentarians and country leaders. (This list is not exhaustive).
            </p>
            <p>
                The basis for nomination could be either long-standing service to a particular cause, or a major project /specific idea that has had significant impact.
            </p>
        </div>

        <div>
            <h2>Evidence</h2>
            <ol>
                <li>Objective; and</li>
                <li>In the public domain.</li>
            </ol>
            <p>Types of evidence could include:</p>
            <ul>
                <li>Quotes from a reputable organisation’s website.</li>
                <li>Copies or referenced quotes from local, regional and national press.
                </li>
                <li>Details of any other awards given, if so cite them as well
                </li>
                <li>Other reputable web source or other sources eg NGO, Governmental etc (Web sources should exclude wikipedia, blogs and unreferenced data if possible). When sourcing web pages, you must state the date that you last accessed the page.
                </li>
            </ul>
            <p>Hard copies of the evidence (including print outs of relevant web pages) must be provided with the nomination form.</p>
            <p>A specimen form can be found here (link to ANNEX 2) that shows a completed form and the type of evidence that could be presented. The specimen form is not exhaustive and is only a sample provided as a guide to help the person making the nomination complete the form.</p>
            <p>The Ahmadiyya Muslim Peace Prize Committee (the ‘central committee’) that will assess the nominations reserves the right to revoke a nomination and review its decision at any stage if new evidence comes to light (based on said criteria) that would have affected its assessment of the nomination.</p>
        </div>

        <div>
            <h2>Process</h2>
            <ol>
                <li>The nominee (ie person being nominated) must be alive at time of nomination.</li>
                <li>All nominations must be submitted in English.</li>
                <li>All nominations must be made on the attached form.</li>
                <li>Nominations can be made by anyone (Ahmadi or non-Ahmadi individuals) and must be submitted through the National Amir of the country in which the person making the nomination resides.</li>
                <li>The National Amir may provide comments on any nomination (if he wishes to do so) and must forward all nominations to the central committee.</li>
                <li>All deliberations of the central committee will remain confidential.</li>
            </ol>
        </div>

        <div>
            <h2>Exclusion</h2>
            <ol>
                <li>Nominations cannot be made by the immediate family members of a nominee.</li>
                <li>Jama’at organisations (including any auxiliary organisations and umbrella subgroups) cannot be nominated.</li>
            </ol>
        </div>

        <div>
            <h2>Timeline</h2>
            <p>If you wish to propose a nominee and for them to be considered for this years prize, the nomination will need to be submitted by the 15th May 2024.</p>
